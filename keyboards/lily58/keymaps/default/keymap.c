void encoder_update_user(uint8_t index, bool clockwise) {
  if (index == 0) {
	if (IS_LAYER_ON(_NAV)) {
		if (clockwise) {
		  tap_code(KC_VOLU);
		} else {
		  tap_code(KC_VOLD);
		}
	} else {
		if (clockwise) {
			tap_code(KC_MS_WH_DOWN);
		} else {
			tap_code(KC_MS_WH_UP);
		}
	}
  }
}

